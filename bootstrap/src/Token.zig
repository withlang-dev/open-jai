pub fn directiveNameOrInvalid(bytes: []const u8) Tag {
    var buf: [128]u8 = undefined;
    if (bytes.len + 1 > buf.len) return .invalid;
    buf[0] = '#';
    @memcpy(buf[1 .. bytes.len + 1], bytes);
    return directiveOrInvalid(buf[0 .. bytes.len + 1]);
}
