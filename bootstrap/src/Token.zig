pub const Tag = Token.Tag;

pub fn keywordOrIdentifier(bytes: []const u8) Tag {
    const keywords = std.StaticStringMap(Tag).initComptime(.{
        .{ "if", .keyword_if }, .{ "else", .keyword_else }, .{ "then", .keyword_then },
        .{ "ifx", .keyword_ifx }, .{ "for", .keyword_for }, .{ "while", .keyword_while },
        .{ "return", .keyword_return }, .{ "break", .keyword_break }, .{ "continue", .keyword_continue },
        .{ "defer", .keyword_defer }, .{ "using", .keyword_using }, .{ "struct", .keyword_struct },
        .{ "union", .keyword_union }, .{ "enum", .keyword_enum }, .{ "enum_flags", .keyword_enum_flags },
        .{ "cast", .keyword_cast }, .{ "xx", .keyword_xx }, .{ "inline", .keyword_inline },
        .{ "no_inline", .keyword_no_inline }, .{ "null", .keyword_null }, .{ "true", .keyword_true },
        .{ "false", .keyword_false }, .{ "void", .keyword_void }, .{ "it", .keyword_it },
        .{ "it_index", .keyword_it_index }, .{ "push_context", .keyword_push_context },
        .{ "operator", .keyword_operator }, .{ "case", .keyword_case }, .{ "size_of", .keyword_size_of },
        .{ "type_of", .keyword_type_of }, .{ "type_info", .keyword_type_info },
        .{ "is_constant", .keyword_is_constant }, .{ "interface", .keyword_interface },
    });
    return keywords.get(bytes) orelse .identifier;
}

